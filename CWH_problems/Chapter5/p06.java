public class p06{
    public static void main(String [] args){
        // using while loop
        int i =0;
        System.out.println("Print using While loop");
        while(true){
            if (i == 5){
                break;
            }
            System.out.println("1");
            i++;
        }
        // using for loop 
            System.out.println("Print using for loop");

        for (int j=0; j<5; j++){
            System.out.println("1");
        }
    }
}